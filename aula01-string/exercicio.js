//Todos as maneiras dão exatamente o mesmo resultado!

// Usando as aspas duplas com barras invertidas, e usando o '+' como concatenação
console.log("Meu nome é \"Eduardo\". Estou aprendendo JavaScript às " + 10 + " da manhã.");

// Usando as aspas duplas com barras invertidas, e usando a ',' como concatenação
console.log("Meu nome é \"Eduardo\". Estou aprendendo JavaScript às", 10, "da manhã.");

// Usando as aspas simples para não precisar usar barra inversa para usar aspas duplas dentro da string
console.log('Meu nome é "Eduardo". Estou aprendendo JavaScript às', 10, 'da manhã.');