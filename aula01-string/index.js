// String = Texto

console.log('Eduardo Idalgo'); // Aspas simples
console.log("Eduardo Idalgo"); // Aspas duplas
console.log(`Eduardo Idalgo`); // Crase // Usado para template strings

console.log("Eduardo 'Idalgo'"); // Aspas simples dentro de uma string iniciada com aspas duplas
console.log('Eduardo "Idalgo"'); // Aspas duplas dentro de uma string iniciada com aspas simples

console.log("Eduardo \"Idalgo\""); // Aspas duplas dentro de uma string iniciada com aspas duplas usando barra inversa
console.log('Eduardo \'Idalgo\''); // Aspas simples dentro de uma string iniciada com aspas simples usando barra inversa

console.log('123456'); // String
console.log("123456"); // String
console.log(123456); // Número inteiro literal
console.log(15.85); // Número flutuante literal

console.log(35, 15.85, "Eduardo Idalgo"); // 35 15.85 Eduardo Idalgo // NUMBER, NUMBER, STRING
